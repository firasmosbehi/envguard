func TestParseEnvFileLinesWithoutEquals(t *testing.T) {
	tmpDir := t.TempDir()
	path := filepath.Join(tmpDir, ".env")
	content := "KEY=value\nINVALID_LINE\n# comment\n\n"
	os.WriteFile(path, []byte(content), 0644)

	entries, lookup, err := parseEnvFile(path)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	if len(entries) != 4 {
		t.Errorf("expected 4 entries, got %d", len(entries))
	}
	if lookup["KEY"].Value != "value" {
		t.Errorf("expected KEY=value, got %q", lookup["KEY"].Value)
	}
	// INVALID_LINE should be in entries but not in lookup
	_, exists := lookup["INVALID_LINE"]
	if exists {
		t.Error("INVALID_LINE should not be in lookup")
	}
}

func TestRunNoDriftWithSchema(t *testing.T) {
	tmpDir := t.TempDir()

	envPath := filepath.Join(tmpDir, ".env")
	os.WriteFile(envPath, []byte("API_KEY=secret123\n"), 0644)

	examplePath := filepath.Join(tmpDir, ".env.example")
	os.WriteFile(examplePath, []byte("API_KEY=***\n"), 0644)

	schemaPath := filepath.Join(tmpDir, "envguard.yaml")
	os.WriteFile(schemaPath, []byte(`
version: "1.0"
env:
  API_KEY:
    type: string
    sensitive: true
`), 0644)

	result, err := Run(Options{
		EnvPath:     envPath,
		ExamplePath: examplePath,
		SchemaPath:  schemaPath,
		Check:       true,
	})
	if err != nil {
		t.Fatalf("Run failed: %v", err)
	}

	// The example content differs from what would be generated, so there IS drift
	if !result.WouldWrite {
		t.Log("Note: WouldWrite is false - example matches expected output")
	}
}

