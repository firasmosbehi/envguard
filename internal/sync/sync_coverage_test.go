func TestGenerateExampleConsecutiveBlanks(t *testing.T) {
	entries := []envEntry{
		{Key: "KEY1", Value: "val1", Raw: "KEY1=val1"},
		{Key: "", IsBlank: true, Raw: ""},
		{Key: "", IsBlank: true, Raw: ""},
		{Key: "KEY2", Value: "val2", Raw: "KEY2=val2"},
	}
	lookup := map[string]envEntry{
		"KEY1": {Key: "KEY1", Value: "val1"},
		"KEY2": {Key: "KEY2", Value: "val2"},
	}
	exampleLookup := make(map[string]envEntry)
	sensitiveSet := make(map[string]bool)

	result := generateExample(entries, []envEntry{}, lookup, exampleLookup, sensitiveSet, nil)
	// Should contain both keys (suggestPlaceholder may transform values)
	if !strings.Contains(result, "KEY1=") {
		t.Error("expected KEY1 in output")
	}
	if !strings.Contains(result, "KEY2=") {
		t.Error("expected KEY2 in output")
	}
	// Count blank lines - should not have consecutive blanks
	// (exclude trailing empty line from final "\n")
	lines := strings.Split(strings.TrimSuffix(result, "\n"), "\n")
	blankCount := 0
	for _, line := range lines {
		if line == "" {
			blankCount++
		}
	}
	// Header has one blank line after comments, plus one between KEY1 and KEY2
	if blankCount > 2 {
		t.Errorf("expected at most 2 blank lines, got %d", blankCount)
	}
}

